#include "libft.h"

long long	ft_atoll(const char *nptr)
{
	long long	result;
	int			signal;
	int			i;

	result = 0;
	signal = 1;
	i = 0;
	while (ft_isspace(nptr[i]))
		i++;
	if (nptr[i] == '+' || nptr[i] == '-')
	{
		if (nptr[i] == '-')
			signal = -signal;
		i++;
	}
	while (ft_isdigit(nptr[i]))
	{
		result = result * 10 + (nptr[i] - '0');
		i++;
	}
	return (result * signal);
}